# Syntax of for loop with range() function in Python.

# for variable in range(start, stop, step_size):
    # code block to be executed
# increments by 1 by default if step_size is not provided.

for i in range (0,4):   # (4) is excluded same as (0 to 3) 
    print(i)
    i += 1

