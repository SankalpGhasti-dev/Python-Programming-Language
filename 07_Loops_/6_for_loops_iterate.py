# A for loop is used to iterate through a sequence like list, tuple, or string.

print("iterating through a list")

l = [34, 53, 634 , 63, 632]

for i in l:
    print(i)

print("iterating through a tuple")

t = (34, 52, 43, 3, 21)

for i in t:
    print(i)

print("iterating through a string")

s = "Sankalp"

for i in s:
    print(i)

# In each of these examples, the for loop goes through each element in the sequence (list, tuple, or string) and assigns it to the variable 'i', which is then printed. This allows us to access and process each element in the sequence one by one.
