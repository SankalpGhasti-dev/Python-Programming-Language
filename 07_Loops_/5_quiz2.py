l = [1, "harry", False, "This", "Rohan", "Shubham", "soham"]

i = 0 

while(i<len(l)):
    print(l[i])
    i += 1

# Logic behind this code is that we start with index 0 and continue until the index is less than the length of the list. In each iteration, we print the element at the current index and then increment the index by 1. This way, we access and print each element of the list one by one.
