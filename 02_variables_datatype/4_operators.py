# Arithmetic Operators

x = 10
y = 3
z = x + y  # Addition
print(z)

# Assignment Operators

a = 5

a += 2  # Increment value of a by 2 and then assign it to a
print("New value of a is now a", a )

a -= 1  # Decrement value of a by 1 and then assign it to a
print("New value of a is now a", a)

a *= 3  # Multiply value of a by 3 and then assign it to a
print("New value of a is now a", a)

a /= 2  # Divide value of a by 2 and then assign it to a
print("New value of a is now a", a)

# Comparison Operators

a = 3>2 
print(a)  # True

b = 3<=2
print(b)  # False

c = 3==2
print(c)  # False

# The results of comparison operators are boolean values: True or False

# Logical Operators

# Truth Table for 'or' operator
print("True or False is ", True or False)  
print("True or True is ", True or False)
print("False or True is ", False or True)
print("False or False is ", False or False)

# Truth Table for 'and' operator
print("True and False is ", True and False)  
print("True and True is ", True and True)   
print("False and True is ", False and True)
print("False and False is ", False and False)

# Truth Table for 'not' operator

print(not(False)) 